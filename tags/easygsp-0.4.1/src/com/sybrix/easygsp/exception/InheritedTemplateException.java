package com.sybrix.easygsp.exception;

/**
 * InheritedTemplateException <br/>
 *
 * @author David Lee
 */
public class InheritedTemplateException extends Exception{

        public InheritedTemplateException() {
                super();
        }
        public InheritedTemplateException(String message) {
                super(message);
        }
        public InheritedTemplateException(String message, Throwable cause) {
                super(message, cause);
        }
        public InheritedTemplateException(Throwable cause) {
                super(cause);    
        }
}
